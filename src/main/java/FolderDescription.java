public class FolderDescription {
    private String jsonContent;
    private int score;

    public void setScore(int score) {
        this.score = score;
    }

    public void setJsonContent(String jsonContent) {
        this.jsonContent = jsonContent;
    }

    public int getScore() {
        return score;
    }

    public String getJSONContent() {
        return jsonContent;
    }
}
